

public class Currency_Meter {


    public void usd_Eur(double usd){
        System.out.print("1 USD = " + 0.95 + " Euro.\n");
        System.out.println(usd + " USD is equal to " +(usd * 0.95) + " Euro." );
    }

    public void gbp_Usd(double gbp){
        System.out.print("1 GBP = " + 1.27 + " Dollar.\n");
        System.out.println(gbp + " GBP is equal to " +(gbp * 1.27) + " Dollar." );
    }

    public void eur_Usd(double eur){
        System.out.print("1 EUR = " + 0.95 + " Dollar.\n");
        System.out.println(eur + " EUR is equal to " +(eur * 0.95) + " Dollar." );
    }

    public void usd_gbp(double usd){
        System.out.print("1 USD = " + 0.79 + " Pound.\n");
        System.out.println(usd + " USD is equal to " +(usd * 0.79) + " Pound." );
    }


}
